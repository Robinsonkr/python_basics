#Sort a list of dictionaries based on the "year" value of the dictionaries
def myfun(ele):
	return ele['year']

cars = [
  {'car': 'Ford', 'year': 2005},
  {'car': 'Mitsubishi', 'year': 2000},
  {'car': 'BMW', 'year': 2019},
  {'car': 'VW', 'year': 2011}
]

cars.sort(key=myfun)
print(cars)

