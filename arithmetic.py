#arithmetic operations

first_num = float(input("Enter first number"))

second_num =  float(input("Enter second number"))
  
sum = first_num + second_num

sub = first_num - second_num

mul = first_num * second_num

div = first_num / second_num

print('sum of {0} and {1} is {2}'.format(first_num,second_num,sum))

print('difference of {0} and {1} is {2}'.format(first_num,second_num,sub))

print('The multiplication of {0} and {1} is {2}'.format(first_num,second_num,mul))

print('The division of {0} and {1} is {2}'.format(first_num,second_num,div)) 