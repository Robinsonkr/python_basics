def addition(a,b):
	result = a+b
	print("{0} + {1} = {2}".format(a,b,result))

def multiplication(a,b):
	result = a*b
	print("{0} * {1} = {2}".format(a,b,result))


def subtraction(a,b):
	result = a-b
	print("{0} - {1} = {2}".format(a,b,result))

def division(a,b):
	result = a/b
	print("{0} / {1} = {2}".format(a,b,result))